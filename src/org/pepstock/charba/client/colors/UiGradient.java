	private final String[] colors;

	/**
	 * Builds a gradient using argument as list of colors in HEX format.
	 * 
	 * @param hexColors list of colors in HEX format
	 */
	private UiGradient(String... hexColors) {
		colors = hexColors;
	}

	/**
	 * Creates a LINEAR gradient, with <code>topDown</code> orientation and <code>chart</code> scope.
	 * 
	 * @return new gradient based on color of UiGradient list
	 */
	public Gradient createGradient() {
		return createGradient(GradientType.LINEAR);
	}

	/**
	 * Creates a gradient by a type, with <code>chart</code> scope.
	 * 
	 * @param type gradient type
	 * @return new gradient based on color of UiGradient list
	 */
	public Gradient createGradient(GradientType type) {
		return createGradient(type, GradientOrientation.getDefaultByType(type));
	}

	/**
	 * Creates a gradient by a type and an orientation, with <code>chart</code> scope.
	 * 
	 * @param type gradient type
	 * @param orientation orientation of gradient
	 * @return new gradient based on color of UiGradient list
	 */
	public Gradient createGradient(GradientType type, GradientOrientation orientation) {
		return createGradient(type, orientation, GradientScope.CHART);
	}

	/**
	 * Creates a gradient by a type and a scope.
	 * 
	 * @param type gradient type
	 * @param scope scope of gradient
	 * @return new gradient based on color of UiGradient list
	 */
	public Gradient createGradient(GradientType type, GradientScope scope) {
		return createGradient(type, GradientOrientation.getDefaultByType(type), scope);
	}

	/**
	 * Creates a gradient by a type, an orientation and a scope.
	 * 
	 * @param type gradient type
	 * @param orientation orientation of gradient
	 * @param scope scope of gradient
	 * @return new gradient based on color of UiGradient list
	 */
	public Gradient createGradient(GradientType type, GradientOrientation orientation, GradientScope scope) {
		// checks if array of colors preloaded is consistent
		if (colors == null || colors.length < 2) {
			// if not, exception
			throw new IllegalArgumentException("Colors list is not consistent");
		}
		// creates a gradient
		Gradient gradient = new Gradient(type, orientation, scope);
		// sets the max length to use on after calculation
		// because the first and last colors have got
		// offset 0D and 1D.
		int maxLength = colors.length - 1;
		// adds the first color
		gradient.addColorStop(0D, colors[0]);
		// checks if there is more then 2 colors
		// becuase the first and last are added
		// out of cycle
		if (colors.length > 2) {
			// calculates the increment of offset
			// for gradient color
			double increment = 1D / maxLength;
			// initial value of offset
			double offset = 0D;
			// scans all colors between the 2nd and
			// the last (excluded)
			for (int i = 1; i < maxLength; i++) {
				// calculates the color offset
				offset += increment;
				// adds colors to gradient
				gradient.addColorStop(offset, colors[i]);
			}
		}
		// adds the last color
		gradient.addColorStop(1D, colors[maxLength]);
		return gradient;
	}

